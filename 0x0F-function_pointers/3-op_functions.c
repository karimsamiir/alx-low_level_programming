#include"3-calc.h"

/**
 * op_add - add 2 integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract 2 integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: difference
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - divide 2 integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: quotient
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module 2 integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: reminder
*/

int op_mod(int a, int b)
{
	return (a % b);
}
